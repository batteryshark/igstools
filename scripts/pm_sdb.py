def GetMoreSongInfo(song_id):
    for entry in SongInfoDB:
        if song_id in entry['Charts'].keys():
            return {
            'Name':entry['Name'],
            'Artist':entry['Artist'],
            'Length':entry['Length'],
            'BPM':entry['BPM'],
            'Moves':entry['Moves'],
            'Stars':entry['Charts'][song_id]['Stars'],
            'Notes':entry['Charts'][song_id]['Notes'],
            'SectionID':entry['Charts'][song_id]['SectionID'],
            'Difficulty':entry['Charts'][song_id]['Difficulty']            
            }

    print("Error - SongID: %d Not Found!" % song_id)
    return None


